void main() {
  //Variables

  //Strings
  String name = "John";

  //Integer
  int age = 41;

  //Dynamic
  dynamic firstname = "Mark";

  //Const and final
  String fullname = name + firstname;
  final String nickname = "Jared";

  print('My name is $fullname and I am $age years old. My nickname is $nickname');

}