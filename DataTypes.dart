void main(){
  //Data Types
  //Numbers, Strings, Booleans, Lists, Maps, Dynamic

  //Strings
  String name = "John Doe";
  print("String: $name");
  //Integers
  int age = 45;
  print("Integer: $age");

  //Doubles
  double percentage = 69.0;
  print("Double: $percentage");

  //Booleans
  bool isOpen = true;
  print("Boolean: $isOpen");

  //Dynamic
  dynamic fullname = "John Elder";
  print("Dynamic: $fullname");
}